package com.ciclos;



public class Ciclo1_OARG {

	public static void main(String[] args) {
		// Programa un algoritmo que realice la tabla de multiplicar del 12
        
		//Declarar variable 
        int n=12;
        
        //Imprimir texto
        System.out.println("Tabla del 12");
        
        //Ciclo 
        for(int i = 1; i<=10; i++){
            //Imprimir resultados 
        	System.out.println(n + " * " + i + " = " + n*i); 
		
		
		
	}

	}
}
