package com.ciclos;

public class Ciclos1_ADCA {
	public static void main(String[] args) {
        //Programa un algoritmo que realice la tabla de multiplicar del 12
        int i = 1;
        int j = 12;
        while(i<=12){
            
            System.out.println(i + " x " + j + " = " + (i * j));
            i++;
        }
    }

}
