package com.condicionales;

import java.util.Scanner;

public class Condicionales6_ADCA {

	public static void main(String[] args) {
		/* La asociación de vinicultores tiene como política fijar un precio inicial 
		 * al kilo
de uva, la cual se clasifica en tipos (A y B), y además en tamaños (1 y 2). 
Cuando se realiza la venta del producto, ésta es de un sólo tipo y tamaño, se 
requiere determinar cuánto recibirá un productor por la uva que entrega en un 
embarque considerando lo siguiente: * Si es de tipo A, se le cargan 20 
céntimos al precio inicial cuando es de tamaño 1 y 30 céntimos si es de 
tamaño 2. * Si es de tipo B, se rebajan 30 céntimos cuando es de tamaño 1, y 
50 céntimos cuando es de tamaño 2. Precio inicial se recibe desde teclado */
		int precioInicial;
		char tipoDeUva;
		char grapeSize;//aqui puse la variable en ingles para evitar unar la letra "ñ"
		Scanner entrada = new Scanner(System.in);
		System.out.println("Indique el precio inicial ");
		precioInicial = entrada.nextInt();
		System.out.println("Indique el tipo de uva (A) ó (B) ");
		tipoDeUva = entrada.next().charAt(0);
		System.out.println("Indique el tamaño de la uva");
		grapeSize = entrada.next().charAt(0);
		
		if(tipoDeUva == 'A' && grapeSize == '1') {
			precioInicial += 20;
			System.out.println(precioInicial);
		}else if(tipoDeUva == 'A' && grapeSize == '2'){
			precioInicial += 30;
			System.out.println(precioInicial);
		}else if(tipoDeUva == 'B' && grapeSize == '1') {
			precioInicial -= 30;
			System.out.println(precioInicial);
		}else if(tipoDeUva == 'B' && grapeSize == '2') {
			precioInicial -= 50;
			System.out.println(precioInicial);
		}else{
			System.out.println("Hubo algun error");
		}
			
	}

}
